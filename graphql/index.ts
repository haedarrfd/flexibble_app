// Get user
export const getUserQuery = `query GetUser($email: Email!) {
  user(by: { email: $email }) {
    id
    name
    email
    avatarUrl
    description
    githubUrl
    linkedinUrl
  }
}`;

// Create user
export const createUserMutation = `
	mutation CreateUser($input: UserCreateInput!) {
		userCreate(input: $input) {
			user {
				name
				email
				avatarUrl
				description
				githubUrl
				linkedinUrl
				id
			}
		}
	}
`;

// Projects Query if there is a category
export const projectsQuery = `
  query GetProjectsCategory($category: String, $endcursor: String) {
    projectSearch(first: 8, after: $endcursor, filter: {category: {eq: $category}}) {
      pageInfo {
        hasNextPage
        hasPreviousPage
        startCursor
        endCursor
      }
      edges {
        node {
          title
          githubUrl
          description
          liveSiteUrl
          id
          image
          category
          createdBy {
            id
            email
            name
            avatarUrl
          }
        }
      }
    }
  }
`;

